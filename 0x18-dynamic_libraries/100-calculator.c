/**
 * add - Adds two integers
 * @a: First integer
 * @b: Second integer
 *
 * Description: Returns the sum of two integers.
 *
 * Return: The sum of integers a and b.
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtracts two integers
 * @a: First integer
 * @b: Second integer
 *
 * Description: Returns the result of subtracting b from a.
 *
 * Return: The result of the subtraction (a - b).
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - Divides two integers
 * @a: Dividend
 * @b: Divisor
 *
 * Description: Returns the result of dividing a by b.
 *
 * Return: The result of the division (a / b).
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - Multiplies two integers
 * @a: First integer
 * @b: Second integer
 *
 * Description: Returns the product of two integers.
 *
 * Return: The product of integers a and b.
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - Computes the remainder of the division of two integers
 * @a: Dividend
 * @b: Divisor
 *
 * Description: Returns the remainder when a is divided by b.
 *
 * Return: The remainder of the division (a % b).
 */

int mod(int a, int b)
{
	return (a % b);
}
